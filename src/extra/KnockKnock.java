package extra;

import javax.swing.JOptionPane;

public class KnockKnock {
public static void main(String[] args) {
	JOptionPane.showMessageDialog(null, "Knock Knock");
	JOptionPane.showMessageDialog(null, "You say 'who's there?'");
	JOptionPane.showMessageDialog(null, "To.");
	JOptionPane.showMessageDialog(null, "You say 'to who?'");
	JOptionPane.showMessageDialog(null, "It's to whom, mind your grammar next time.");
}
}
